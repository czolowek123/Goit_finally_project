function getFilteredProducts() {
    var filtered = [];
    for (var i = 0; i < products.length; i++) {
        var p = products[i];
        if (activeCollection !== 'all' && p.collection !== activeCollection) continue;
        var numPrice = parseInt(p.price.replace(/\s/g, ''));
        if (numPrice < priceMin) continue;
        if (numPrice > priceMax) continue;
        filtered.push(p);
    }

    if (activeSort === 'price-asc') {
        filtered.sort(function(a, b) {
            return parseInt(a.price.replace(/\s/g, '')) - parseInt(b.price.replace(/\s/g, ''));
        });
    } else if (activeSort === 'price-desc') {
        filtered.sort(function(a, b) {
            return parseInt(b.price.replace(/\s/g, '')) - parseInt(a.price.replace(/\s/g, ''));
        });
    } else if (activeSort === 'name-asc') {
        filtered.sort(function(a, b) {
            return a.name.localeCompare(b.name);
        });
    } else if (activeSort === 'name-desc') {
        filtered.sort(function(a, b) {
            return b.name.localeCompare(a.name);
        });
    }

    return filtered;
}

