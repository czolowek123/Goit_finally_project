function handleSearchInput(input) {
    var query = input.value.toLowerCase().trim();
    var results = document.getElementById('search-results');
    if (!results) return;

    if (query.length < 1) {
        clearSearchResults();
        return;
    }

    var html = '';
    var found = false;

    for (var i = 0; i < products.length; i++) {
        var p = products[i];
        var displayName = p.nameKey ? t(p.nameKey) : p.name;
        var searchText = (displayName + ' ' + t(p.materialKey) + ' ' + t(p.stoneKey) + ' ' + p.collection + ' ' + p.name).toLowerCase();
        if (searchText.indexOf(query) !== -1) {
            html += '<a href="/product/' + p.id + '/" class="search-result-item">'
                + '<img src="/static/images/' + p.images[0] + '" class="search-result-img">'
                + '<div>'
                + '<div class="search-result-name">' + displayName + '</div>'
                + '<div class="search-result-material">' + t(p.materialKey) + '</div>'
                + '</div>'
                + '</a>';
            found = true;
        }
    }

    for (var j = 0; j < searchablePages.length; j++) {
        var pg = searchablePages[j];
        var pageText = (t(pg.name) + ' ' + pg.keywords).toLowerCase();
        if (pageText.indexOf(query) !== -1) {
            html += '<a href="' + pg.url + '" class="search-result-item">'
                + '<div class="search-result-page-icon">●</div>'
                + '<div>'
                + '<div class="search-result-name">' + t(pg.name) + '</div>'
                + '<div class="search-result-material">' + pg.url + '</div>'
                + '</div>'
                + '</a>';
            found = true;
        }
    }

    if (!found) {
        html = '<div style="padding:20px 40px;color:#999;font-size:14px;letter-spacing:1px;">No results</div>';
    }

    results.innerHTML = html;
    results.classList.add('active');
}

