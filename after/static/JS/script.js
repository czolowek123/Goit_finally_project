/* ============================================================
   SLIDER — переключение фото в карточке товара
   ============================================================ */

function initSliderLogic(container, imageCount) {
    if (imageCount <= 1) return;

    var currentSlide = 0;
    var sliderTrack = container.querySelector('.slider');
    var nextBtn = container.querySelector('.next');
    var prevBtn = container.querySelector('.prev');

    if (nextBtn && prevBtn) {
        nextBtn.addEventListener('click', function(e) {
            e.stopPropagation();
            currentSlide = (currentSlide + 1) % imageCount;
            sliderTrack.style.transform = 'translateX(-' + (currentSlide * 100) + '%)';
            updateDots(container, currentSlide);
        });

        prevBtn.addEventListener('click', function(e) {
            e.stopPropagation();
            currentSlide = (currentSlide - 1 + imageCount) % imageCount;
            sliderTrack.style.transform = 'translateX(-' + (currentSlide * 100) + '%)';
            updateDots(container, currentSlide);
        });
    }
}

function updateDots(container, activeIndex) {
    var dots = container.querySelectorAll('.slide-dot');
    for (var i = 0; i < dots.length; i++) {
        dots[i].classList.remove('active');
    }
    if (dots[activeIndex]) {
        dots[activeIndex].classList.add('active');
    }
}

