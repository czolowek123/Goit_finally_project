function t(key) {
    if (TRANSLATIONS[key] && TRANSLATIONS[key][currentLang]) {
        return TRANSLATIONS[key][currentLang];
    }
    if (TRANSLATIONS[key] && TRANSLATIONS[key]['ru']) {
        return TRANSLATIONS[key]['ru'];
    }
    return key;
}

function applyLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('hf_lang', lang);

    var els = document.querySelectorAll('[data-i18n]');
    for (var i = 0; i < els.length; i++) {
        var key = els[i].getAttribute('data-i18n');
        var val = t(key);
        if (val.indexOf('<') !== -1) {
            els[i].innerHTML = val;
        } else {
            els[i].textContent = val;
        }
    }

    var langLabel = document.getElementById('lang-current');
    if (langLabel) {
        langLabel.textContent = lang.toUpperCase() + ' >';
    }

    var dropdown = document.getElementById('lang-dropdown');
    if (dropdown) {
        dropdown.classList.remove('active');
    }

    var grid = document.getElementById('product-grid');
    if (grid && grid.style.display !== 'none') {
        grid.innerHTML = '';
        renderProductGrid();
    }

    var detailContainer = document.getElementById('product-detail-view');
    if (detailContainer && detailContainer.innerHTML !== '') {
        renderProductDetails();
    }

    renderCartList();
}

