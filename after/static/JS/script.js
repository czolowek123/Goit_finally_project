var currentUser = null;

function submitLogin() {
    var emailInput = document.querySelector('#auth-login-form input[type="email"]');
    var passInput = document.querySelector('#auth-login-form input[type="password"]');
    var errorEl = document.getElementById('auth-login-error');
    if (!emailInput || !passInput) return;

    var email = emailInput.value.trim();
    var password = passInput.value;

    if (!email || !password) {
        if (errorEl) errorEl.textContent = 'Заполните все поля';
        return;
    }

    fetch('/api/login/', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ email: email, password: password })
    })
    .then(function(r) { return r.json(); })
    .then(function(data) {
        if (data.ok) {
            currentUser = data.user;
            updateAuthHeaderUI();
            closeAuthModal();
            emailInput.value = '';
            passInput.value = '';
            if (errorEl) errorEl.textContent = '';
        } else {
            if (errorEl) errorEl.textContent = data.error || 'Login failed';
        }
    })
    .catch(function() {
        if (errorEl) errorEl.textContent = 'Connection error';
    });
}

function submitRegister() {
    var firstNameInput = document.querySelector('#auth-register-form input[placeholder="Your first name"]');
    var lastNameInput = document.querySelector('#auth-register-form input[placeholder="Your last name"]');
    var emailInput = document.querySelector('#auth-register-form input[type="email"]');
    var passInput = document.querySelector('#auth-register-form input[type="password"]');
    var errorEl = document.getElementById('auth-register-error');

    var email = emailInput ? emailInput.value.trim() : '';
    var password = passInput ? passInput.value : '';
    var firstName = firstNameInput ? firstNameInput.value.trim() : '';
    var lastName = lastNameInput ? lastNameInput.value.trim() : '';

    if (!email || !password) {
        if (errorEl) errorEl.textContent = 'Заполните Email и пароль';
        return;
    }

    fetch('/api/register/', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({
            email: email,
            password: password,
            first_name: firstName,
            last_name: lastName
        })
    })
    .then(function(r) { return r.json(); })
    .then(function(data) {
        if (data.ok) {
            currentUser = data.user;
            updateAuthHeaderUI();
            closeAuthModal();
            if (errorEl) errorEl.textContent = '';
        } else {
            if (errorEl) errorEl.textContent = data.error || 'Registration failed';
        }
    })
    .catch(function() {
        if (errorEl) errorEl.textContent = 'Connection error';
    });
}

function doLogout() {
    fetch('/api/logout/', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' }
    })
    .then(function() {
        currentUser = null;
        updateAuthHeaderUI();
    });
}

function checkAuth() {
    fetch('/api/user/')
    .then(function(r) { return r.json(); })
    .then(function(data) {
        if (data.ok && data.authenticated) {
            currentUser = data.user;
            updateAuthHeaderUI();
        }
    })
    .catch(function() {});
}

function handleGoogleCredential(response) {
    if (!response || !response.credential) return;

    fetch('/api/google-auth/', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ credential: response.credential })
    })
    .then(function(r) { return r.json(); })
    .then(function(data) {
        if (data.ok) {
            currentUser = data.user;
            updateAuthHeaderUI();
            closeAuthModal();
        } else {
            alert(data.error || 'Google login failed');
        }
    })
    .catch(function() {
        alert('Connection error');
    });
}

function updateAuthHeaderUI() {
    var accountBtns = document.querySelectorAll('.account-btn');
    var loggedInEls = document.querySelectorAll('.logged-in-info');

    if (currentUser) {
        var displayName = currentUser.first_name || currentUser.email.split('@')[0];
        for (var i = 0; i < accountBtns.length; i++) {
            accountBtns[i].style.display = 'none';
        }
        for (var j = 0; j < loggedInEls.length; j++) {
            loggedInEls[j].style.display = 'flex';
            var nameSpan = loggedInEls[j].querySelector('.user-display-name');
            if (nameSpan) nameSpan.textContent = displayName;
        }
    } else {
        for (var i = 0; i < accountBtns.length; i++) {
            accountBtns[i].style.display = '';
        }
        for (var j = 0; j < loggedInEls.length; j++) {
            loggedInEls[j].style.display = 'none';
        }
    }
}

function initGoogleSignIn() {
    var googleBtns = document.querySelectorAll('.auth-google-btn');
    var googleClientId = document.querySelector('meta[name="google-client-id"]');
    var hasClientId = googleClientId && googleClientId.content;
    var hasGoogleLib = typeof google !== 'undefined' && google.accounts;

    if (hasClientId && hasGoogleLib) {
        google.accounts.id.initialize({
            client_id: googleClientId.content,
            callback: handleGoogleCredential
        });
        for (var i = 0; i < googleBtns.length; i++) {
            (function(btn) {
                btn.addEventListener('click', function(e) {
                    e.preventDefault();
                    google.accounts.id.prompt();
                });
            })(googleBtns[i]);
        }
    } else {
        for (var i = 0; i < googleBtns.length; i++) {
            googleBtns[i].style.display = 'none';
            var divider = googleBtns[i].previousElementSibling;
            if (divider && divider.classList.contains('auth-divider')) {
                divider.style.display = 'none';
            }
        }
    }
}

